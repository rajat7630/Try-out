                px-4 border border-red-700 rounded">
                Send Test
              </button>
            </a>
            <button
              on:click={deleteTestHandler}
              class="deleteButton bg-red-500 hover:bg-red-700 text-white
              font-bold py-2 px-4 border border-red-700 rounded">
              Delete
            </button>
          </div>
        </div>

      </div>

    {:catch err}
      Error: {err}
    {/await}
